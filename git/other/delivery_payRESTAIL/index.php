               <p></p>
               <p align="justify"><strong></strong>
               </p>
               <p align="justify"><strong><img alt="pochta RF.jpg" src="/upload/medialibrary/3e1/3e11907e08ca06927960a78483227169.jpg" title="pochta RF.jpg" width="51" height="54">Расходы </strong> на доставку товаров в большей степени определяются <strong> тарифами Почты РФ. </strong>
