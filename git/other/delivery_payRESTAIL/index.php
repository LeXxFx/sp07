               <p align="justify"><strong> Что важно знать при выборе доставки с помощью Почты России? </strong>
               </p>
               <p align="justify"><img alt="pochta RF.jpg" src="/upload/medialibrary/3e1/3e11907e08ca06927960a78483227169.jpg" title="pochta RF.jpg" width="51" height="54"> После оформления, комплектации и оплаты, ваш заказ передается на <strong> Почту России. </strong>
